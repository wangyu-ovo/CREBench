static uint8_t sbox_decoded[256];
static int sbox_ready;

__attribute__((noinline, noclone))
static void ensure_sbox_decoded(void) {
    if (sbox_ready) {
        return;
    }
    constxor_compiler_barrier();
    materialize_u8_table(sbox_xor_lhs, sbox_xor_rhs, sbox_decoded, 256);
    constxor_compiler_barrier();
    sbox_ready = 1;
}

const uint8_t *constxor_skipjack_sbox(void) {
    ensure_sbox_decoded();
    return sbox_decoded;
}
