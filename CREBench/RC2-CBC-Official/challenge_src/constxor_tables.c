static uint8_t key_table_decoded[256];
static int key_table_ready;

__attribute__((noinline, noclone))
static void ensure_key_table_decoded(void) {
    if (key_table_ready) {
        return;
    }
    constxor_compiler_barrier();
    materialize_u8_table(key_table_xor_lhs, key_table_xor_rhs, key_table_decoded, 256);
    constxor_compiler_barrier();
    key_table_ready = 1;
}

const uint8_t *constxor_rc2_key_table(void) {
    ensure_key_table_decoded();
    return key_table_decoded;
}
