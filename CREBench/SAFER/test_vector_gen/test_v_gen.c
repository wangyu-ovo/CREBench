        memset(ciphertext, 0, sizeof(ciphertext));
        safer_cbc_encrypt(&ctx, iv, plaintexts[i], ciphertext, sizeof(ciphertext));
        fprintf(out, "  {\n    \"plaintext\": \"");
        for (size_t j = 0; j < sizeof(plaintexts[i]); j++) fprintf(out, "%02x", plaintexts[i][j]);
